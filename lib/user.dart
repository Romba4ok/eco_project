class CustomUser {
  final String name;
  final String state;
  final String city;
  final String email;
  final String password;
  final String id;
  final String avatar;
  final String user_user;

  CustomUser({
    required this.name,
    required this.state,
    required this.city,
    required this.email,
    required this.password,
    required this.id,
    required this.avatar,
    required this.user_user,
  });
}
